## Data Categorisation

'''
1) Whole Number (Ints) - 100, 1000, -450, 999
2) Real Numbers (Floats) - 33.33, 44.01, -1000.033
3) String - "Bangalore", "India", "Raj", "abc123"
4) Boolean  - True, False

Variables in python are dynamic in nature

'''

a = 10
print(a)
print(type(a))

a = 10.33
print(a)
print(type(a))

a = 'New Jersey'
print(a)
print(type(a))
